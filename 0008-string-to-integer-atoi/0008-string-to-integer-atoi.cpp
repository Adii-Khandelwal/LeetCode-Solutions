class Solution {
public:
    int myAtoi(string s) {
        
         int len = s.size();
        double num = 0;
        int i=0;
        while(s[i] == ' '){
            i++;
        }
        bool positive = s[i] == '+';
        bool negative = s[i] == '-';
        positive == true ? i++ : i;
        negative == true ? i++ : i;
        while(i < len && s[i] >= '0' && s[i] <= '9'){
            num = num*10 + (s[i]-'0');
            i++;
        }
        num = negative ? -num : num;
        if(num>INT_MAX)return INT_MAX;
        if(num<INT_MIN)return INT_MIN;
        return num;
        
    }
};